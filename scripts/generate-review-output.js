function formatScannerResults(scanMatches) {
  if (scanMatches.length === 0) return ["No actionable cases detected."];

  const lines = [];
  for (const match of scanMatches) {
    lines.push(`- ${match.relativePath}`);
    for (const reason of match.reasons) {
      lines.push(`  - \`${reason.kind}\` line ${reason.line} \`@${reason.trigger}\``);
    }
  }
  return lines;
}
